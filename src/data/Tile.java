package data;

public class Tile {
	private int x;
	private int y;
	
	private int  size;
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int tileSize) {
		this.size = tileSize;
		
	}
	
	

}
